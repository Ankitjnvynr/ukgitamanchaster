<script>
  // Counting Animation
  document.addEventListener("DOMContentLoaded", function() {
    const countText = document.querySelector('.count-text');
    const stopValue = parseInt(countText.getAttribute('data-stop'));
    const speed = parseInt(countText.getAttribute('data-speed'));
    let count = 0;

    const counter = setInterval(() => {
      count++;
      countText.textContent = count + (count < stopValue ? '' : '+');
      if (count >= stopValue) {
        clearInterval(counter);
      }
    }, speed / stopValue);
  });
</script>
